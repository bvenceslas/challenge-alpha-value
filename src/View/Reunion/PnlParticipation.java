    private void jLabel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel3MouseClicked
        try
        {
            String names[] = new String[list1.getItemCount()];
            
            for (int i = 0; i < list1.getItemCount(); i++) 
            {
               names[i] =  list1.getItem(i);
            
                person.setNom(names[i]);
                reunion.setDesignation(cmbReunion.getSelectedItem().toString());
                participation.setPerson(person);
                participation.setReunion(reunion);
                if (participation.Update())
                {                    
                    Fill_Object.remplir_tableau(jTable1, "select r.designation,CONCAT(nom,' ',postnom,' ',prenom) AS Participant from tParticipation pa\n" +
                    "inner join tPerson p on p.id = pa.id_Person inner join tReunion r on r.id = pa.id_Reunion");
                }
            }
                JOptionPane.showMessageDialog(null,"pr√©sence effectu√©e", "Reussite", JOptionPane.INFORMATION_MESSAGE);
        }
        catch (Exception e)
        {
            JOptionPane.showMessageDialog(null,"Erreur trouv√©e"+ e.getMessage(), "Echec |||", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jLabel3MouseClicked

